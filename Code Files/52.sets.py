# Using curly braces
my_set = {1, 2, 3, 4, 4, 5}
print(f"Set created with curly braces: {my_set}")

# Using the set() constructor with a list
another_set = set([3, 4, 5, 5, 6, 7])
print(f"Set created with set() constructor: {another_set}")

# Creating an empty set (important: use set() for empty sets, not {})
empty_set = set()
print(f"Empty set: {empty_set}")